public class UtilidadMatematica {
    
    public static double redondearNum(double num){
        double numRedondeado = Math.round(num);
        return numRedondeado;
    }
    
    public static double numeroAbsoluto(double num){
        double numAbsoluto=Math.abs(num);
        return numAbsoluto;
    }
}
